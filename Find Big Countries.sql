-- Afghanistan Asia 652230 25,500,100 20,343,000,000
-- Albania Europe 28,748 2,831,741 12,960,000,000
-- Algeria Africa 2,381,741 37,100,000 188,681,000,000
-- Andorra Europe 468 78,115 3,712,000,000
